                Neige en plaine : pourquoi a-t-elle souvent du mal à tenir au sol  ? 
        </p>
        <h3>L'influence océanique, la grande responsable</h3>
        <p>
            Comme résumé sur le schéma, voici ce qu'il se passe lors de la mise en place d'une perturbation neigeuse :<br>

            De l'air <strong>>froid modéré</strong> et humide arrive du Nord-Ouest. À l'origine, il fait froid jusqu'au sol, et la <strong>neige </strong>tombe sans problème jusqu'à 0m d'altitude. Ces chutes de neige s'approchent des côtes françaises. Problème : l'océan se <strong>réchauffe rapidement</strong>, et on passe d'une eau à 3-4 °C à une eau à plus de 10°C. L'air froid au contact de ces eaux chaudes se réchauffe un peu, mais seulement sur quelques dizaines de mètres (0 à 400m d'altitude, par exemple). <br>
            La perturbation neigeuse arrive sur la France, l'air froid débarque jusqu'au Grand Est. Mais il y a un problème : entre 0 et 400 ou 400m, l'air "doux" provoqué par l'océan n'a pas eu le temps de se refroidir à nouveau, et il "glisse" le long du sol jusqu'à notre région.
        </p>
        <p>
            Résultat : il fait entre 1 et 3°C à Metz, Reims ou Nancy, et c'est de la <strong>pluie-neige voire de la pluie</strong>. Seule la montagne connaît encore des températures favorables à une belle neige. En prenant un peu d'altitude, l'air froid d'origine a été peu voire pas du tout "chauffé" par l'océan. Dans ces conditions, la neige tombe librement et en quantité dès 500 à 600m en général. Ce qui explique pourquoi il neige bien plus dans les Vosges que dans le reste de la région. <br>
            Quand la masse d'air est encore plus froide, le contraste thermique est encore plus fort : dans ces conditions, une simple butte de 300m d'altitude peut connaître <strong>plusieurs centimètres</strong> de neige alors qu'il n'y a <strong>rien</strong> dans la vallée.
        </p>
        <h3>Pourquoi les prévisions sont-elles difficiles dans ces conditions ?</h3>
        <p>
            De l'ouest de la France à la région parisienne, la difficulté est moindre : dès qu'il y a une influence de l'océan, il ne neige pas. Ce qui explique aussi pourquoi il neige rarement sur ces régions. Dans le Grand Est, c'est différent, surtout en Alsace/Lorraine : l'altitude moyenne est plus haute (180 à 300m pour la plaine et nombreux plateaux à 400m contre 10 à 200m pour l'ouest). Et surtout la <strong>continentalisation de notre région</strong> : souvent de l'air plus <strong>froid est présent et résiste plus longtemps</strong> à l'arrivée d'une perturbation et en fonction de sa résistance, l'épisode neigeux sera +/- important.<br>
            Tout l'enjeu est donc de trouver l'équilibre le plus probable qui peut permettre ou non des chutes de neige ainsi que l'altitude. Quand dans nos bulletins, nous expliquons que les "hauteurs sont favorisées pour cet épisode", vous saurez à présent que l'influence océanique est présente.
        </p>
