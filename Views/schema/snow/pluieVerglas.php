        <p class="p-intro">
            C'est un phénomène peu courant mais particulièrement impactant. Les éiposdes de 1996, 2013 ou encore 2024 ont marqués les esprits. Il faut des conditions très précises et une organisation des masses d'air inhabituelle pour créer un environnement propice a sa formation. Voici une explication générale qui s'applique à tous les épisodes de ce type en France.
        </p>
        <h3>Explications</h3>
        <p>
            Un ou plusieurs jours avant l'épisode, un froid, souvent important, est présent sur la région concernée. La présence d'un ciel dégagé permet un rayonnement tellurique important (la chaleur du sol se transmet vers l'espace) et un gel en profondeur. Une perturbation accompagnée d'un redoux approche des régions figées par le froid.<br>
            En temps normal, l'air doux rencontre l'air froid et cela provoque des chutes de neige temporaires suivies de l'arrivée de la pluie progressivement. Dans le cas des pluies surfondues, un vent continental (souvent de nord-est/est) se glisse sous la perturbation et "rampe" à la surface du sol. Cet air est très dense et constamment renouvelé par la présence d'une bise constante. Dans ce contexte, entre le sol et +50 à +500m, une couche d'air froid dense reste bloquée avec des températures allant de -0.5 à -3°C. En altitude, au niveau des nuages, l'air est doux et la pluie tombe généreusement. En arrivant en bas, elle tombe sur un <strong>sol gelé en profondeur </strong> par plusieurs jours de gel, et cela provoque du verglas. En plus de cela, l'air froid ambiant persistant empêche ce verglas de se résorber, et la glace se forme ensuite sur la végétation.
        </p>
        <h3>Comment se termine t'il ?</h3>
        <p>
            Il faut attendre que le vent change de direction au sol pour que l'épisode de verglas se termine. Souvent, cette transition est <strong> brutale (+5 à +10°C en 10 km) </strong> accompagnée de vent.
            <br>
            Ce phénomène peut durer <strong>plusieurs heures</strong>, comme on a pu le constater lors du dernier épisode de janvier 2024. Toutefois, dans la majorité des cas, il reste localisé et avec une durée limitée. Des phénomènes de cette ampleur sont peu courants : 1996, 2013 et donc à présent 2024 sont des années où un phénomène de cette ampleur a eu lieu.
        </p>
