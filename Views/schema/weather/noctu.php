        <h3> De quoi s'agit il ?</h3>
        <p>
            Ce sont des <strong>nuages de très haute altitude</strong> formant une toile très fine un peu à l'image des cirrus. Ils se forment dans la mésosphère, la dernière véritable couche de l'atmosphère avant l'espace, elle se situe entre <strong>50 et 90 km d'altitude.</strong>
            La formation de ces nuages reste encore très floue, une théorie possible serait une retombée des poussières d'étoiles filantes et autres en provenance de l'espace vers cette couche. En rencontrant des poches d'humidités, ces poussières favorisent la condensation et donc à la formation de cristaux de glace. Il existe différents types de ce nuage en fonction des conditions présentes, comme pour nos nuages classiques.

        </p>
        <h3>Quelles sont les différences avec nos nuages classiques?</h3>
        <p>
            Les nuages météorologiques ordinaires <strong>se forment exclusivement dans la troposphère</strong>, une fine couche de l'atmosphère qui se situe entre le sol et 10 à 13 km d'altitude. Ils sont soumis à des mécanismes généralement bien connus et leurs observations sont très simples depuis le sol et leurs processus de formation très différents.<br>
            Les noctulescents se situent au "dessus du ciel bleu" et ils sont donc complètement invisibles. Seul des conditions très précises permettent leurs observations.
        </p>
        <h3>Comment les observer ?</h3>
        <p>
            Les conditions d'apparitions de ce type de nuage sont les suivantes:
            ▪️ Se situer entre le 50e et le 65e de latitude (Pays Bas, Danemark..) <br>
            ▪️ Ciel bien clair entre le 1er Juin et le 1er Août ( de préférence proche du solstice donc entre le 10 Juin et le 1er Juillet) <br>
            ▪️Entre <strong> 22h15 et 23h45 </strong> ou entre <strong>3h30 et 4h45</strong> en regardant vers le Nord <br>
            ▪️La France étant trop au sud, les observations sont rares. Cependant, les régions du Nord dont l'Alsace/Lorraine, les chances sont plus élevées mais restes très peu fréquentes. <br>
            ▪️ Depuis les sommets des Vosges pour une observation optimale
        </p>
        <h3>Leurs apparitions cachent-elles un soucis ?</h3>
        <p>
            C'est un phénomène tout à fait naturel, bien que de les voir est assez surprenant et inhabituel ils ne sont en aucuns cas un signal d'un problème climatique.<br>
            A ne pas confondre avec un autre nuage de très haute altitude, il s'agit des nuages nacrés. Ils se forment plus bas entre 15 et 25 km d'altitude dans la stratosphère. Cependant, il est quasi impossible d'être observé en France et ils sont présents au cœur de l'Hiver
        </p>

