        <p class="p-intro">
            La grêle, ce phénomène orageux très redouté allant d'une taille de bille à la balle de golf ou d'un pamplemousse. Mais comment expliquer la différence de taille ?
        </p>
        <h3>Explications</h3>
        <p>
            Lors de la formation d'un orage, l'air chaud entre en ascension entrainant de la vapeur formant des gouttelettes d'eau qui, au fur et à mesure qu'elles grimpent en altitude, entrent en surfusion et/ou se transforment en grésil.
            Une fois une certaine altitude vers le sommet de l'orage, elles vont entreprendre leurs chute. A travers celle-ci, elles vont rencontrer d'autres gouttes surfondues qui vont générer <strong>un noyaux de congélation</strong> et vont s'agglutiner. Loin du courant ascendant les grêlons vont finir par fondre et se transformer en pluies.<br>
            Les plus proches du courant ascendant peuvent se voir être repris à travers celui-ci qui va commencer à les faire <strong>tourner au sein du nuage.</strong> Les grêlons vont alors augmenter en volume et finir par tomber une fois qu'ils seront trop lourds pour le courant ascendant. La zone violette sur le schéma représente (en théorie) l'endroit le plus sujet à voir des grêlons de plus grosses tailles suivie derrière par les précipitations grêligène de plus petites tailles et ou complétée/suivie par les pluies.
            <br>
            <br>
            Vous comprendrez alors que plus la vitesse du courant ascendant est élevée, plus il sera capable de reprendre le grêlon. Dans un orage classique, le grêlon tourne rarement plus d'un tour ou deux voir jamais. Par contre dans des orages plus complexes tels <strong>les "supercellules"</strong>, le courant ascendant puissant peut engendrer les plus <strong>gros grêlons et créer des ravages.</strong>

        </p>
        <h3>Les conditions de formation</h3>
        <p>
            Pour qu'un orage soit suffisamment puissant pour supporter de tels grêlons, il faut des conditions atmosphériques particulières. Les ingrédients sont complexes, mais deux grands facteurs vont jouer un rôle important : <strong> une couche d'air sec en altitude </strong>  et une <strong> forte énergie disponible.</strong> 
        </p>
        <p>
            En effet, la présence d'une poche d'air très sec (humidité inférieure à 40 %), lors du trajet des précipitations (pluie, neige ou grêle), va permettre une forte évaporation. Ce phénomène, aussi appelé "isothermie", va venir refroidir l'air ambiant sur une hauteur parfois importante. Les grêlons fondent donc moins rapidement, ce qui permet des grêlons plus imposants. La présence d'une forte énergie (ou de chaleur humide) donne de la "force" à l'orage. Dans des contextes de chaleur humide, l'air et l'humidité absorbent cette chaleur sous forme d'énergie et elle est restituée dans l'orage. Cela permet une forte intensification des courants ascendants et donc une capacité accrue à retenir les grêlons dans leur chute, comme expliqué plus haut. <br>
            En France, il est fréquent de retrouver des grêlons mesurant <strong>de 2 à 5 cm</strong>  ; plus rarement de 5 à 8 cm et de manière exceptionnelle de <strong> 8 à 12 cm.</strong> 
        </p>
