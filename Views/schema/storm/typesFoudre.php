        <h3>Les descendants</h3>
        <p>
            Nous classons les éclairs de type "nuage-sol" en <strong> ascendants et descendants </strong>, bien que le principe de formation de la foudre réside sur le contact entre une décharge venant du nuage et une du sol. Cette décharge se nomme :<strong> traceur.</strong> (Illustré en traceur ascendant sur l'arbre.)
            Pour <strong>80 à 90 % des cas de foudre en Europe</strong>, c'est l'éclair descendant négatif qui domine contre 10 à 20 % pour l'éclair descendant positif.
            Si tous les <strong>éclairs nuage-sol</strong> se rejoignent, pourquoi les appeler alors "ascendants" et "descendants" ?<br>
            Et pourquoi négatifs et positifs ?
            <br>
            Lorsque nous avons un éclair descendant (le coup de foudre classique), ce sont deux traceurs qui se rejoignent. Cependant, les charges négatives et positives n'ont pas forcément la même place. Lorsqu'un <strong>éclair négatif descendant</strong> se produit c'est un traceur négatif partant du nuage qui initie le processus et se dirige vers le sol tandis que un arc en retour positif se dirige vers le nuage. On parle alors de transfert de charges négatives vers le sol.<br>
            Le visuel du coup de foudre sera celui que tout le monde connait avec peu ou <strong>beaucoup de ramifications</strong>.
            A l'inverse le <strong>coup de foudre positif</strong> sera initié par un traceur positif dans le nuage (venant souvent de très haut dans le nuage) et de charges négatives venant du sol. On parle alors de transfert de charges positives vers le sol.
            Le visuel est aveuglant, il est linéaire, aspect lissé. C'est un coup de foudre puissant et dangereux avec un tonnerre explosif. Ils peuvent atteindre les 150 000 ampères voir dépasser les 500 000. On parle alors de superbolt ou mégabolt.
        </p>
        <h3>Les ascendants</h3>
        <p>
            Les<strong> éclairs ascendants</strong> reprennent le principe des éclairs descendants mais cette fois, c'est le sol qui initie un traceur positif ou négatif vers le nuage. Ce type d'éclair est rare et s'observe principalement depuis des antennes relais/ télécommunications, grattes ciel, viaduc, haute montagne...
            Ils sont souvent en V ou Y voir en forme d'arbre géant.
        </p>
